package blackjack.poker;

/**
 * Class {@code Suit} enumerates four suits in poker
 */
public enum Suit {
    /**
     * has the smallest value
     */
    CLUB,
    /**
     * has the second smallest value
     */
    DIAMOND,
    /**
     * has the second largest value
     */
    HEART,
    /**
     * has the largest value
     */
    SPADE,
}
